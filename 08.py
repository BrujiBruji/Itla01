#Suma
def one():
    return 1
def two():
    return 2
valorOne = one()
valorTwo = two()
suma = valorOne + valorTwo
print("Valor de suma:", suma)

#Resta
def tres():
    return 3
def cuatro():
    return 4
valorTres = tres()
valorCuatro = cuatro()
resta = valorTres - valorCuatro
print("Valor de resta:", resta)

#Multiplicacion
def cinco():
    return 5
def seis():
    return 6
valorCinco = cinco()
valorSeis = seis()
mult = valorCinco * valorSeis
print("Valor de multiplicacion:", mult)

#Division
def siete():
    return 7
def ocho():
    return 8
valorSiete = siete()
valorOcho = ocho()
division = valorSiete / valorOcho
print("Valor de division:", division)
